#Loop over a list
fruits = ["Orange", "Apple", "Banana", "Mango"]

for f in fruits:
    print(f) #prints each fruit

#Loop with index using range()
#Prints number from 0 to 10
for i in range(11):
    print(i)
    

print("Even Numbers")
#Loop with start, end, skip
#Print Even Numbers from 0 to 50
for i in range(0,51,2):
    print(i)
