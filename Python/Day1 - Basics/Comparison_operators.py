"""
Comparison Operators:- 
-> These operators are used to compare the Values or Operands.
-> These Operators always return Boolean Values
"""

a = eval(input("Enter the 1st Value: "))
b = eval(input("Enter the 2nd Value: "))

#Greater Than
print(f"{a} > {b} = ",a>b)

#Less Than
print(f"{a} < {b} = ",a<b)

#Equal to
print(f"{a} == {b} = ",a==b)

#Not Equal to
print(f"{a} != {b} = ",a!=b)

#Greater Than or Equal to
print(f"{a} >= {b} = ",a>=b)

#Less Than or Equal to
print(f"{a} <= {b} = ",a<=b)